package com.example.json.parser;

public class BackupStorageAttributeDetails {

	private String clientName;
	private String policyName;
	private String policyType;
	private String mbytes;
	private String storageUnitName;
	private String storageType;
	
	public String getClientName() {
		return clientName;
	}
	public void setClientName(String clientName) {
		this.clientName = clientName;
	}
	public String getPolicyName() {
		return policyName;
	}
	public void setPolicyName(String policyName) {
		this.policyName = policyName;
	}
	public String getPolicyType() {
		return policyType;
	}
	public void setPolicyType(String policyType) {
		this.policyType = policyType;
	}
	public String getMbytes() {
		return mbytes;
	}
	public void setMbytes(String mbytes) {
		this.mbytes = mbytes;
	}
	public String getStorageUnitName() {
		return storageUnitName;
	}
	public void setStorageUnitName(String storageUnitName) {
		this.storageUnitName = storageUnitName;
	}
	public String getStorageType() {
		return storageType;
	}
	public void setStorageType(String storageUnitType) {
		this.storageType = storageUnitType;
	}
	
}
